def decode(year: int, input_path: Path) -> pd.DataFrame:
    """Shift-JIS CSVを読み込み、緯度経度を10進変換したDataFrameを返す。"""
    raw = pd.read_csv(input_path, dtype=object, encoding='cp932').values.tolist()
    print(f"  読み込み: {len(raw)}件")

    mapper = _map_row_2022plus if year >= 2022 else _map_row_old

    rows = []
    skipped = 0
    for row in raw:
        mapped = mapper(row)
        if mapped is None:
            skipped += 1
        else:
            rows.append(mapped)

    if skipped:
        print(f"  スキップ（緯度経度不正）: {skipped}件")

    return pd.DataFrame(rows, columns=FIELDNAMES)
