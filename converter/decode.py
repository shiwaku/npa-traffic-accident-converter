def _dms_to_decimal(lat_str: str, lng_str: str):
    """度分秒文字列を10進度に変換。桁数不正はNoneを返す。"""
    if len(lat_str) != 9 or len(lng_str) != 10:
        return None, None
    clat = float(lat_str[0:2]) + float(lat_str[2:4]) / 60 + (float(lat_str[4:9]) / 1000) / 3600
    clng = float(lng_str[0:3]) + float(lng_str[3:5]) / 60 + (float(lng_str[5:10]) / 1000) / 3600
    return clat, clng


